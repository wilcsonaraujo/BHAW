package tis.bhaw.Classes;

public class Materia {
	
	private String id;
	private String nome;
	
	public Materia() {}
	
	public Materia(String nome) {
		this.nome = nome;
	}

	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	

}
